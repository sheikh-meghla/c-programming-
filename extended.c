#include<stdio.h>
int main()
{
    int normalInt = 10;
    long longInt = 10000000000;
    short shortInt = 5;
    unsigned int unsignedInt = 20;
    printf("Normal Integer: %d\n", normalInt);
    printf("Long Integer: %lld\n", longInt);
    printf("Short Integer: %d\n", shortInt);
    printf("Unsigned Integer: %u\n", unsignedInt);
    return 0;

}